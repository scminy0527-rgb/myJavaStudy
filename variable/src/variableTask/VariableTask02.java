package variableTask;

public class VariableTask02 {
   public static void main(String[] args) {
//      5, 7845.15, false, B, 85.10F, 자바, S, 98590, D, true, 공백문자
//      저장공간 2개만 사용해서 모든 값을 출력하기
//      datas, numbers
      double numbers = 0.0;
      String datas = null;
      
      datas = "5";
      System.out.println(datas);
      
      numbers = 7845.15;
      System.out.println(numbers);
      
      datas = "false";
      System.out.println(datas);
      
      datas = "B";
      System.out.println(datas);
      
      numbers = 85.10F;
      System.out.println(numbers);
      
      datas = "자바";
      System.out.println(datas);
      
      datas = "S";
      System.out.println(datas);
      
      datas = "98590";
      System.out.println(datas);
      
      datas = "D";
      System.out.println(datas);
      
      datas = "true";
      System.out.println(datas);
      
      datas = " ";
      System.out.println(datas);
   }
}











